# Tuples are the collection of ordered and un-mutable data
# for tuples no brackets are mandatory
# The value inside a Tuple is separated by a coma(,)
# Multiple data types can be written inside a tuple

A = "Apple", "Mango", "Banana"
print(type(A))

B = "Banana", #By adding the coma we can make the string tuple
print(type(B))